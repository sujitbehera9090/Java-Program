

import java.util.*;
import java.util.*;

class Fibonasci_series{

    public static void main( String arg[] ){

        int range , n1 = 0 , n2 = 1 , fibo;

        for( int i = 1; i <= range; i++ ){

            fibo = n1 + n2;

            n1 = n2;

            n2 = fibo;

        }

        
    }
}
